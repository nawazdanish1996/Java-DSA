package com.company;

import java.util.*;

public class TimeComplexity03 {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();

        // i = 0 to n
        // Outer Loop
        for(int i=0; i<n; i++){
            System.out.println("Hello");
        }
        // j = 0 to m
        // Outer Loop
        for (int j=0; j<m; j++){
            System.out.println("Hello");
        }
    }
}
