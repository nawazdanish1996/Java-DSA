// Java Programming Syntex

// ** Java Syntex
// 1. Package      2. Class
// 3. Method       4. Code Statement

// ** Class - name should start with an uppercase letter.
// main                 Wrong       Main                Right
// Myclass              Wrong       MyClass             Right
// learnjava            Wrong       LearnJava           Right
// Findareaoftriangle   Wrong       FindAreaOfTriangle  Right

// ** Method - name should start with a lowercase letter
// Main                 Wrong       main                 Right
// Myclass              Wrong       myClass              Right
// learnjava            Wrong       learnJava            Right
// Findareaoftriangle   Wrong       findAreaOfTriangle   Right

// ** public static void main(String [] args){}
// public: Access modifier
// static: No need to create object to call main method.
// void: does not require any return type
// main(): Method name
// String [] args: String array - name of the array: args (arguments).
// {}: Body of the Method


// Package
package com.company;

// Class
public class JavaSyntex00 {
    //Method
    public static void main(String[] args) {
        // Code Statement
        System.out.println("Hello World");
    }
}